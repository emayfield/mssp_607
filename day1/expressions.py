# Expressions

# The following lines are the simplest kind of expression: When you enter them
# into interactive python, they only return themselves.

# The first kind of expression is an integer literal
4
0

# We can also have string literals, enclosed either by single quotes or double quotes
"book"
'cat'

# Next, we have boolean literals, with only two possible values: True and False
True
False

# If you run this file on the command line, nothing will print out so far. We have 
# never given an instruction to the computer to display any information back to us.
# In order to do that, we can use the simplest function: print()

print(2 + 2)
print("book")

